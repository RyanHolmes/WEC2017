// turn a string into a 2d array of numbers
function buildMatrix(str){
  let returnArray = []
  // groups of 4
  let tempMatrix = str.match(/(.{1,4})/g);
  //turn into chars into numbers
  for (var i = 0; i < 4; i++){
    returnArray[i] = tempMatrix[i].split('').map(function(e){
      if(e.charCodeAt(0) - 65 === 16) {
        return 15;
      } else {
        return e.charCodeAt(0) - 65;
      }
    });
  }
  return returnArray;
}
//render the grid
function drawGrid(matrix, options=0){
  var returnBlocks = []
  //loop matrix
  for(var j = 0; j < 4; j++){
    for(var k = 0; k < 4; k++){
      //if open space, make red
      if(matrix[j][k] === 15) {
        var tempRect = new Rect(k * side + options + 300, j * side + 150, side, side)
        .fill(`red`);
        returnBlocks.push(tempRect);
      //change rgb value based on order number so solved puzzle is a gradient
      } else {
        var tempRect = new Rect(k * side + options + 300, j * side + 150, side, side)
        .fill(`rgb(${ 128 - matrix[j][k] * 8 }, ${ 128 - matrix[j][k] * 8} , ${ 128 - matrix[j][k] * 8 })`);
        returnBlocks.push(tempRect);
      }
    }
  }
  return returnBlocks;
}
























